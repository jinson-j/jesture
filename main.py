import argparse
from multigesture import run_multiplayer_mode
from gesture import run_singleplayer_mode

def main():
    parser = argparse.ArgumentParser(description='Jesture - Gesture-based drawing game')
    parser.add_argument('--mode', '-m', choices=['single', 'multi'], default='single',
                        help='Game mode: single for one player, multi for two players')
    parser.add_argument('--time', '-t', type=int, default=30,
                        help='Drawing time in seconds (default: 30)')
    
    args = parser.parse_args()
    
    if args.mode == 'single':
        print(f"Starting single player mode...")
        run_singleplayer_mode()
    elif args.mode == 'multi':
        print(f"Starting multiplayer mode with {args.time} seconds drawing time...")
        run_multiplayer_mode(drawing_time=args.time)

if __name__ == "__main__":
    main()

